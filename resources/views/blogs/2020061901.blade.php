<div class="container mb-4" id="#1st">
  <h5>キャッシュフロー計算書の読み方-概要</h5>
  <div  style="padding-left: 1em;">

    <p class="my-2">次に、キャッシュフロー計算書の読み方について進めていきます。</p>
    <p>
      キャッシュフロー計算書は企業の持っている<u>現金</u>の流れを表します。企業の「営業活動」「投資活動」「財務活動」にての<u>現金ベースでの</u>収支がわかります。キャッシュフローが＋になっていれば現金の増加、-になっていれば現金の減少が見られます。
    </p>
    <p>覚えなければならないのは以下のことです。</p>
    <ul>
      <ul>
        <li>総資本営業キャッシュフロー：どれだけの財産規模で、どれだけの営業キャッシュフローを生み出しているかを計算する指標となります。：営業CF/総資産</li>
        <li>キャッシュフローマージン：売上高のうち何割がキャッシュによって得られたのかを示している指標です。目安は15%以上。営業キャッシュフロー÷売上高</li>
        <li>フリーキャッシュフロー「自由に使えるお金」という意味です。 企業が事業活動を通じて得た資金のうち、自由に使える額を意味します。：営業活動によるキャッシュフロー ＋ 投資活動によるキャッシュフロー</li>
      </ul>
    </ul>

