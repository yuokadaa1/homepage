    <p>例えばトヨタ自動車(7203)であればトヨタ自動車の自社サイトの<a href="https://global.toyota/jp/ir/library/">IRライブラリ</a>というページか<a href="https://disclosure.edinet-fsa.go.jp/">EDINET</a>にて「書類検索」のタブから「書類提出者／有価証券発行者／ファンド情報を指定する」にて「7203」で検索すれば参照することができます。</p>

