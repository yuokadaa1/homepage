  <h5>損益計算書の読み方-概要</h5>
  <div  style="padding-left: 1em;">

    <p class="my-2">次に、損益計算書の読み方について進めていきます。</p>
    <p>
      損益計算書は会社の資本を何に使い幾ら利益（損失）を出したかを表現する決算書です。
    </p>
    <p>覚えなければならないのは以下のことです。</p>
    <ul>
      <ul>
        <li>総資本経常利益率：別名:ROA)総資産に対する利益率を示す指標。保有する資産でどれだけ収益を上げているかを表す。10%以上が望ましい。5%は欲しいところ。（経常利益（「営業利益」＋「営業外収益」－「営業外費用」）／総資本）</li>
        <li>自己資本当期利益率：(別名:ROE)当期利益を自己資本で割った指標。自己資本を使ってどのくらい儲けられたかを表す。20%以上が望ましい。15%程度でも良い範囲：当期純利益／自己資本</li>
        <li>売上総利益率：(別名:粗利率)売上総利益の売上に対する利幅を表す指標である。企業の収益性を判断するための基本的な値となり、競合と比較する事により評価される事が多い。小売業:30%、卸:20%、サービス業:50%～80%等。売上総利益(売上高－売上原価)／売上高</li>
        <li>売上営業利益率：　企業の本業での収益力を表す指標。粗利から人権費を差し引くため本業での実際の儲けが表される。当値が過去3年間ずっと低下している会社は要注意。上場企業平均:3～4%。営業利益／売上高</li>
        <li>売上経常利益率：　企業経営の総合的な利益率を表す指標。企業全体が収益力があるかを同業他社との比較や自社の過去の実績との比較する際に使用される。　平均製造業:4～5%、卸・小売業:2～3%。経常利益／売上高</li>
        <li>売上当期利益率：　当期純利益を売上高で割った指標。会社全体が利益力があるかを同業他社との比較や自社の過去の実績との比較する際に使用される。当期利益／売上高</li>
        <li>損益分岐点安全度：　(別名:BEP)「利益額＝経費」つまり収支ゼロの地点を表した指標。値が低ければ低いほど収益性が高く、かつ売上減少に耐える力が強いことを意味し経営が安定していると判断される。8割程度が理想。固定費／（1－（変動費／売上高））
        </li>
        <li>限界利益率：　売上高に占める限界利益を表した指標。当値が高いということは会社が利益体質にあることを意味する。当値がマイナスということは売れれば売れるほど損をします。限界利益＝売上高－売上高×変動比率（変動費／売上高）</li>
      </ul>
    </ul>

