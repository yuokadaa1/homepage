<div class="container mb-4" id="ZERO">
  <h5>決算資料とは何か</h5>
  <div  style="padding-left: 1em;">

    <p class="my-2">決算資料とは株主に対する会社の報告書です。</p>
    <p>株式市場上場企業には株主に対する報告の義務があり、定まった様式に則って作成されたものが決算資料です。</p>
    <p>決算資料には以下の資料があり、これにより会社の業績や財産を開示しています。</p>
    <ul>
      <li>貸借対照表(BS:BalanceSheet)</li>
      <li>損益計算書(PL:Profit and Loss Statement)</li>
      <li>株主資本等変動計算書(SS:Statement of changes in shareholders' equity)</li>
      <li>キャッシュフロー計算書(CF:Cash flow statement)</li>
    </ul>

  </div>
</div>


