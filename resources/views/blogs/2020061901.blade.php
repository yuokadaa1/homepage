<div class="container mb-4" id="#1st">
  <h5>貸借対照表の読み方-概要</h5>
  <div  style="padding-left: 1em;">

    <p class="my-2">貸借対照表の読み方について一般的なものに絞って解説します。</p>
    <p>
      貸借対照表は企業にどういった種類の資本が累積しているかを表現する決算資料です。
    </p>
    <p>覚えなければならないのは以下のことです。</p>
    <ul>
      <li>資産の部＝決算日時点の会社の保有する財産。負債の部＋純資産。</li>
      <ul>
        <li>流動資産＝資産の部の詳細費目：現金または1年以内に現金化可能な資産</li>
        <li>固定資産＝資産の部の詳細費目：1年以内に現金化不能な資産</li>
      </ul>
      <li>負債の部＝決算日時点の会社の保有する負債。</li>
      <ul>
        <li>流動負債＝の部の詳細費目：1年以内に返却しなければならない負債</li>
        <li>固定負債＝負債の部の詳細費目：1年以内に返却しなくてよい負債</li>
      </ul>
      <li>純資産　＝自己資本（銀行／株主からの出資金＋過去からの利益）。</li>
    </ul>
    <p>資産の部内の細かい費目は都度ググって覚えていくしかないでしょう。</p>
  </div>
</div>

