<div class="container mb-4" id="ZERO">
  <h5>円安・円高とは何か</h5>
  <div  style="padding-left: 1em;">

    <p class="my-2">円の価値が外国の貨幣と相対的に価値が下がるのが円安、価値が上がるのが円高です。</p>
    <p>今日の為替相場を1$=100円、明日の相場を1$=150円、明後日の相場を1$=50円とします。</p>
    <p>明日の1$=150円が今日と比較して円安の状態、明後日の1$=50円を今日と比較して円高の状態です。</p>
    <p>何がどうかというと、仮に日本で100円のビールを今日アメリカに輸出した場合、100円=1$で販売が可能です（※）。</p>
    <p>明日は1$=150円→100円=0.667$での販売となり、明後日は1$=50円→100円=2$での販売になります。</p>
    <p>円安＝円の価値の低下、円高＝円の価値の上昇といったん考えてください。</p>
    <p>※単純化するため輸出コストやらはすべて考えません</p>

  </div>
</div>


<div class="container mb-4" id="#1st">
  <h5>なぜ為替相場が変動するか</h5>
  <div style="padding-left: 1em;">

    <p class="my-2">日本の商品に人気があれば円の価値が上がるからです。</p>
    <p>日本のビールは円がなければ購入できませんので、外国での購入者が増えれば円を買う人が増えます。</p>
    <p>円の発行総量は決まっていますので、購入者が増えれば円の価値が上がり円高になります。</p>
    <p>反対に、購入者が減れば円安になります。</p>
    <p>商品がビールだったり車だったり金融商品だったりするわけですが、円でできることが魅力的→円高になるという概念で覚えて問題ありません。</p>
    <p>そこから発展した学説が購買力平価となります。</p>

  </div>
</div>


<div class="container mb-4" id="1st">
  <h5>購買力平価について</h5>
  <div  style="padding-left: 1em;">

    <p>通貨の価値を決定する説として購買力平価（PPP：purchasing power parity）というものがあります。</p>
    <div class="card card-body" >
      <p>
        同一の商品は生産国でも海外でも同一の値段で販売されるとした際（※一価一物の法則）に、生産国と海外での価格差は為替相場が吸収し均衡する。
        この均衡した為替相場を購買力平価と言います。
      </p>
      <p>
        ※一価一物の法則はすべとの販売可能な商品があらゆる市場で自由に取引された際に、いずれの国でも価格が同一になるという法則。
        どこに行っても同一のものが買える＝どこでも値段が同一になるというもの。
      </p>
    </div>
    <p>これを具体的な指標としたものがビッグマック指数です。</p>

  </div>
</div>

<div class="container mb-4" id="2nd">
  <h5>ビッグマック指数について</h5>
  <div style="padding-left: 1em;">
    <p class="my-2">
      購買力平価を単純化したものがビッグマック指数と言われています。
    </p>
    <div class="card card-body" >
      <p>
        ビッグマックは、材料や調理法などが世界共通で、アメリカでも、日本でも、中国でも、同じものが販売されている。ビッグマック指数は、同じ商品なら同じ価格で販売されるという原則（一物一価の法則）は国境を越えても共通であり、為替相場はこれを実現するように決まるという考えに基づいて、為替相場の適正水準を算出するもの。
      </p>
    </div>
    <p>マクドナルドは多くの国（私は<a href="https://www.jollibee.com.ph/menu/burger//">ジョリビー</a>が好きです）で展開しており、同一の商品が売られています。</p>
    <p>また、国によってハイブランド商品になったりせず、ある程度似たような価値として販売されています。</p>
    <p>そのため、同一の商品が別の通貨で取引されているとして比較の対象になったものです。</p>
    <p>5.74＄ = ビッグマック = 390円が価値としてイコールで結ばれているという考えです。</p>
    <p>この場合、ドル円の適正水準は1$ = 67.94円ということになります。</p>
    <p>実際に比較したものを見てみましょう（表記は2019年7月のものです。）。</p>

