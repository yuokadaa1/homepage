<div class="container mb-4" id="3rd">
  <h5>金利が及ぼす影響について</h5>

  <div style="padding-left: 1em;">
    <p class="my-2">
      金利が下がると通貨安が発生し、金利が上がると通貨高が発生します。
    </p>
    <p>為替の動向の要因として金利差が挙げられます</p>
    <div class="container mb-4" >
      <div class="card card-body" >
      為替相場決定理論の1つとして金利平価（IRP：Interest Rate Parity）として挙げられています。
      「為替レートは自国通貨と外国通貨の名目金利の差によって決定される」という説です。
      </div>
    </div>
    <p>一般的な消費者による銀行預金と同様に投資家も金利が高い国の通貨を購入とします。</p>
    <p>金利の高い国の国債を購入＝その国の通貨での購入となるため、金利の高い国の通貨が購入されます。</p>
    <p>a</p>
    <p>政策金利（正しくは市中金利）の上昇→債権（国債）債権（国債）価格の下落です。</p>
    <p>米国の金利が上昇→国債の金利が上昇→米国債に投資が集中＝ドル買い→ドル高＝円安</p>
    <p>さて、現在の政策金利は日本-0.1%、米国0.25%です。</p>
    <p>政策金利と価値が直接紐づいているものは債権（国債）です。</p>


  </div>
</div>


<div class="container mb-4" id="3rd">
  <h5>株価が及ぼす通貨変動について</h5>
  <div style="padding-left: 1em;">

    <p class="my-2">
      米国株の上昇→ドル高＝円安→日本株の上昇。
    </p>
    <p>逆もしかりです。</p>
    <p>米国株の下落→ドル安＝円高→日本株の下落。</p>
    <p>日本株の上昇→円安＝ドル高→米国株の上昇。</p>
    <p>日本株の下落→円高＝ドル安→米国株の下落</p>
    <p>記載は影響準です。</p>
    <p>日本株の売買の6割は外国人投資家が行っていると言われています。</p>
    <p>そのため、株価の値動きは外国人が握っています。</p>
    <p>米国株が上昇すると米国や日本を含んだ諸国で米国株が買われます。</p>
    <p>米国株購入のために米ドルが買われ相対的に円安になります。</p>
    <p>円安になると日本株の相対的な株価が下がるので日本株が買われます。</p>
    <p>たとえばトヨタ株(7203.t)の株価が1株7,000円として、1$=100円の時は70$、1$=50円の時は140$、1$=200円の時は35$での購入になります。</p>
    <p>ここで、トヨタ株の会社の価値に変動がない場合、会社の価値に変動がないのに株価が倍になったり半分になっています。</p>
    <p>為替相場の変動はまた元にもどるので1$=200円の時に100株購入(1株=35$→100×35$=3,500$)して1$=100円(1株70$→100×70$=7,000$)の時に売ればそれだけで3,500$の儲けがでます。</p>
    <p>また、米国株が上昇した際に上昇幅がこれ以上見込めないと判断した場合、まだ上昇幅の大きい国の株を購入したほうがよいという判断からも買いが増えます。</p>
    <p>余談（？）ですが、日本の投資家は長期売買の割合が多いので、売買量の6割が外国人でも保有量の6割が外国人というわけではありません。</p>
    <p>日銀のETF買入量が増えているので外国人投資家の割合はこれより減っているはずです。</p>
    <p>はじめにでも書きましたが、最近はこの連動も薄れてきているともいわれていますので注意は必要です。</p>

  </div>
</div>



<div class="container mb-4" id="3rd">
  <h5>地政学リスクについて</h5>

  <div style="padding-left: 1em;">
    <p class="my-2">
      日本に打撃の起こりうる地政学リスクの発生は円高を誘発する。
    </p>
    <p>どこでもそうでしょうが日本にも定期的に大規模な天災やら人災が発生します。</p>
    <p>また、一時期よく北朝鮮が日本に向けてミサイルを飛ばすという戦争のリスクも発生しました。</p>
    <p>その時為替がどう動いたかというと、<u><a style="color:red;">一時的に円高に向けて解消の気配が見えると元に戻る</a></u>という動きを見せました。</p>
    <p>これらは日本の生産活動にダメージが発生し、日本国も日本国民も購買能力も輸出能力も低下し、通貨としての魅力が下がる＝円安になるはずです。</p>
    <p>では相場の世界ではなぜこのような事態になったかという話です。</p>
    <p>いくつか説があるようですが、納得のいくものを記載します。</p>

    <p class="my-2">円キャリートレードの巻き戻し</p>
    <p>日本に危機が起きると円キャリートレードの巻き戻しが発生します。</p>
    <div class="card card-body" >
      <p>円キャリートレードとは</p>
      <p>外国の投資家が低金利の日本の銀行から投資資金を円で借りて円をドルなどに交換、交換したドルで日本国外で投資を行うこと。</p>
      <p>アメリカで投資資金を借りた場合の利息が4~5%、日本で投資資金を借りた場合1~2%と言われていますので、日本で借りたほうがコストが低い。</p>
    </div>
    <p class="mt-2">
      円キャリートレード目的で日本円を借りていた外国人投資家がリスクを察知し投資を手放し、投資されなくなった円を抱えることになり円の保有者が増え円高に向かうというものです。
    </p>

    <p class="my-2">生損保会社の資金確保</p>
    <p>日本の生損保会社による、外国資産の売却を含めた資金確保（円）が発生するといわれています。</p>
    <p>日本人が死傷すると日本の生損保会社は保険金を支払う必要性が生じます。</p>
    <p>日本人の生保加入率は8割、損保加入率は7割と言われています。</p>
    <p>死亡保険金をざっくり1人1,000万円と仮定して、10,000人保険加入者が亡くなれば（東日本大震災の死亡者数=15,894人）100億円支払う必要があります（※）。</p>
    <p>これらを準備するため、保険会社が海外に保有している資産を売り払い日本円に変更するとみなされたようです。</p>
    <p>実際は保険金支払準備金（保険金を支払うために保険会社で積み立てているお金）が充分だったため、海外資産の売却は行われなかったため円高の状態から元に戻りました。</p>

    <p>※実際は必要ありません。</p>
    <p>保険は約款上（＝契約）ではおおむね<u><a style="color:red;">戦争、地震、噴火、津波は免責事項</a></u>としています。</p>
    <p>保険会社の許容できる負荷を超えるような事態で死傷者が出た場合は保険金を支払えませんというものです。</p>
    <p>といっても、<a href="https://ja.wikipedia.org/wiki/保険金不払い問題">保険金不払問題</a>などもありましたが保険会社は人気商売。</p>
    <p>実際は保険金が支払われています。</p>
    <p>生命保険協会に加入している保険会社（大手すべて加入）は阪神・淡路大震災（1995年）、東日本大震災（2011年）、御嶽山噴火（2014年）、熊本地震、鳥取県中部地震（2016年）について保険金を支払っています。</p>

